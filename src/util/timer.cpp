#include "timer.h"
#include <time.h>

/*----------------------------------------------------------------------------*/

class TimerPriv {
public:
  clock_t begin;
  clock_t end;
};

/*----------------------------------------------------------------------------*/

Timer::Timer() {
  m = new TimerPriv;
  m->begin = clock();
  m->end = 0;
}

/*----------------------------------------------------------------------------*/

Timer::~Timer() {
  delete m;
}

/*----------------------------------------------------------------------------*/

void Timer::stop() {
  m->end = clock();
}

/*----------------------------------------------------------------------------*/

float Timer::getSec() const {
  return m->end ? (float(m->end-m->begin)/CLOCKS_PER_SEC) : 0;
}
